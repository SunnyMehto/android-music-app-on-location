public class LocationInfo {
    private String sLocationName;
    private String sLongitude;
    private String sLatitude;

    public String getLocationName() {
        return sLocationName;
    }

    public void setLocationName(String sLocationName) {
        this.sLocationName = sLocationName;
    }

    public String getLongitude() {
        return sLongitude;
    }

    public void setLongitude(String sLongitude) {
        this.sLongitude = sLongitude;
    }

    public String getLatitude() {
        return sLatitude;
    }

    public void setLatitude(String sLatitude) {
        this.sLatitude = sLatitude;
    }
}
